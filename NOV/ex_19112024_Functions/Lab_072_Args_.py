def print_mult_arguments(*args):#*args-->list
 for i in args:
     print(i)

print_mult_arguments("Mehak")
print_mult_arguments("Abhi","Mehak","Chirag","Billa","Bobby","Vineet")
print_mult_arguments("Abhi","Mehak","Chirag","Billa","Bobby","Vineet","Golu","Noni")

